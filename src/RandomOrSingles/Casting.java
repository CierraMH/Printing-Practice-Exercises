package RandomOrSingles;

public class Casting {
    public static void main(String[] args) {
        // Created the Scanner for reading user input
        double doubleNumber = 112.35;
        int intNumber = 112;
        System.out.println(doubleNumber);
        System.out.println(intNumber);

        String stringData = "49";
        int number = Integer.parseInt(stringData);
        System.out.println("The string value is: " + stringData);
        System.out.println("The integer value is: " + number);
    }
}